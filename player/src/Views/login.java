    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = this.campoEmail.getText();
        String senha = new String(this.campoSenha.getPassword());
        
        Usuario u = new Usuario();
        u.setEmail(email);
        u.setSenha(senha);
        
        try {
            UsuarioController uc = new UsuarioController();
            boolean res = uc.checarLogin(u);
            
            if(res){
                JOptionPane.showMessageDialog(null, "Login efetuado com sucesso!");
                new playerlistenermp3().setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Email ou senha inv√°lido!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

